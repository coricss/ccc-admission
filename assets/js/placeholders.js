$(document).ready(function() {
	$('#suffixx').css('color','#6c757d');
	$('#suffixx').change(function() {
	var current = $('#suffixx').val();
	if (current != 'null') {
		$('#suffixx').css('color','black');
	} else {
		$('#suffixx').css('color','#6c757d');
	}
	}); 
});
