                  </p>
                    <ul class="qualification-text">
                      <li>The student passed at least 36 academic units with a general weighted average of 2.25 of better for all collegiate academic units taken.</li>
                      <li>The studeent will have to complete not less than 50% of the remaining units in the chosen program in the College</li>
                      <li>The program Director has the prerogative on the admission of transferees with exceptional cases (i.e., Grades of 5.0, dropped, and INC) which will be dealt on a case-to-case basis.</li>
